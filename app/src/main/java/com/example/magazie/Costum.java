package com.example.magazie;

public class Costum {
    public String nume;
    public int numar;
    public String gen;

    public Costum() {
    }

    public Costum(String nume, int numar, String gen) {
        this.nume = nume;
        this.numar = numar;
        this.gen = gen;
    }

    public String getNume() {
        return nume;
    }

    public int getNumar() {
        return numar;
    }

    public String getGen() {
        return gen;
    }
}
